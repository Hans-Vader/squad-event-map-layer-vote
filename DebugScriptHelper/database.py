def save_voting_history(guild_id: int, channel_id: int, all_suggestions: list,
                        winning_layer: Optional[dict]):
    """Save completed event to voting history."""
    conn = _get_conn()
    with conn:
        conn.execute(
            """INSERT INTO voting_history (guild_id, channel_id, all_suggestions, winning_layer)
               VALUES (?, ?, ?, ?)""",
            (guild_id, channel_id, _dumps(all_suggestions),
             _dumps(winning_layer) if winning_layer else None),
        )
    conn.close()


def get_recent_history(guild_id: int, channel_id: int, limit: int = 10) -> list[dict]:
    """Return recent voting history entries."""
    conn = _get_conn()
    rows = conn.execute(
        """SELECT id, all_suggestions, winning_layer, completed_at
           FROM voting_history
           WHERE guild_id = ? AND channel_id = ?
           ORDER BY completed_at DESC LIMIT ?""",
        (guild_id, channel_id, limit),
    ).fetchall()
    conn.close()
    return [
        {
            "id": row[0],
            "all_suggestions": _loads(row[1]),
            "winning_layer": _loads(row[2]) if row[2] else None,
            "completed_at": row[3],
        }
        for row in rows
    ]


def delete_voting_history_entry(entry_id: int) -> bool:
    """Delete a single voting_history row. Returns True if a row was removed."""
    conn = _get_conn()
    with conn:
        cur = conn.execute("DELETE FROM voting_history WHERE id = ?", (entry_id,))
        removed = cur.rowcount > 0
    conn.close()
    if removed:
        logger.info(f"Voting history entry deleted: id={entry_id}")
    return removed


def get_blocked_suggestions(guild_id: int, channel_id: int, lookback: int) -> list[dict]:
    """Return all suggestions from the last `lookback` events for blocking.

    Includes both the full list of suggestions made during each event AND the
    recorded winning_layer, so that manual history entries that only carry a
    winner still get blocked.
    """
    history = get_recent_history(guild_id, channel_id, limit=lookback)
    blocked = []
    for entry in history:
        for suggestion in entry.get("all_suggestions", []):
            blocked.append(suggestion)
        winner = entry.get("winning_layer")
        if winner:
            blocked.append(winner)
    return blocked


# Guild-settings keys that get snapshotted into event["config"] at creation
# time. After Phase 2, every layer suggestion event carries its own copy of
# these values so admins can edit them per-event via the DM edit dialog
# without affecting other events or the guild defaults.
EVENT_CONFIG_KEYS: tuple[str, ...] = (
    "allowed_gamemodes",
    "blacklisted_maps",
    "blacklisted_factions",
    "blacklisted_units",
    "max_suggestions_per_user",
    "max_total_suggestions",
    "history_lookback_events",
)


def _snapshot_event_config(settings: Optional[dict]) -> dict:
    """Build the per-event config dict from a guild settings dict."""
    src = settings or DEFAULT_GUILD_SETTINGS
    snapshot: dict = {}
    for key in EVENT_CONFIG_KEYS:
        value = src.get(key, DEFAULT_GUILD_SETTINGS.get(key))
        # Defensive copy for mutable types so later edits don't bleed into
        # the guild settings dict.
        if isinstance(value, list):
            snapshot[key] = list(value)
        elif isinstance(value, dict):
            snapshot[key] = dict(value)
        else:
            snapshot[key] = value
    return snapshot


def build_default_event(suggestion_start_time=None,
                        settings: Optional[dict] = None) -> dict:
    """Build a fresh event data dict.

    `settings` is the guild settings dict at creation time; the event
    captures a snapshot of the relevant keys (see EVENT_CONFIG_KEYS) so
    each event has its own independently-editable configuration.
    """
    return {
        "phase": "created",
        "event_message_id": None,
        "suggestion_start_time": suggestion_start_time,
        "suggestion_end_time": None,
        "suggestion_duration_seconds": None,
        "voting_start_time": None,
        "voting_duration_hours": 24,
        "poll_message_id": None,
        "max_voting_layers": 10,
        "selected_for_vote": [],
        "winning_layer": None,
        "allow_multiple_votes": False,
        "suggestions": [],
        # Empty list = all configured sources allowed for this event. Set by
        # the admin at /create_layer_suggestion time (defaults to the guild's
        # allowed_sources setting).
        "allowed_sources": [],
        # Per-event snapshot of guild settings that influence the suggestion
        # flow. Edited via the Admin → Edit DM dialog without affecting other
        # events. Readers should call _event_settings(event, guild_settings)
        # in bot.py to merge this with guild fallbacks.
        "config": _snapshot_event_config(settings),
    }
