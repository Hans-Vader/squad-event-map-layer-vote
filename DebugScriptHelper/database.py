def clear_layer_cache():
    """Delete all cached layers."""
    conn = _get_conn()
    with conn:
        conn.execute("DELETE FROM layer_cache")
    conn.close()


def _source_filter(allowed_sources: Optional[list[str]], prefix: str = " AND ") -> tuple[str, list]:
    """Build an SQL fragment + params for filtering by source.

    Returns ("", []) when no filter should be applied (None or empty list).
    Otherwise returns (f"{prefix}source IN (?,?,...)", [src1, src2, ...]).
    """
    if not allowed_sources:
        return "", []
    placeholders = ",".join("?" * len(allowed_sources))
    return f"{prefix}source IN ({placeholders})", list(allowed_sources)


def upsert_layer(raw_name: str, source: str, map_name: str, map_id: str, gamemode: str,
                 layer_version: Optional[str], factions: list,
                 team1_alliances: list, team2_alliances: list,
                 map_size_km: Optional[float] = None):
    """Insert or update a single layer in the cache.

    Layers are uniquely identified by (raw_name, source) — the same rawName
    may legitimately appear in multiple sources.
    """
    conn = _get_conn()
    with conn:
        conn.execute(
            """INSERT INTO layer_cache
               (raw_name, source, map_name, map_id, gamemode, layer_version,
                map_size_km, factions_json, team1_allowed_alliances,
                team2_allowed_alliances, cached_at)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, datetime('now'))
               ON CONFLICT(raw_name, source) DO UPDATE SET
                 map_name=excluded.map_name, map_id=excluded.map_id,
                 gamemode=excluded.gamemode, layer_version=excluded.layer_version,
                 map_size_km=excluded.map_size_km,
                 factions_json=excluded.factions_json,
                 team1_allowed_alliances=excluded.team1_allowed_alliances,
                 team2_allowed_alliances=excluded.team2_allowed_alliances,
                 cached_at=excluded.cached_at""",
            (raw_name, source, map_name, map_id, gamemode, layer_version,
             map_size_km, json.dumps(factions),
             json.dumps(team1_alliances), json.dumps(team2_alliances)),
        )
    conn.close()


def get_layer_cache_count() -> int:
    """Return number of cached layers."""
    conn = _get_conn()
    row = conn.execute("SELECT COUNT(*) FROM layer_cache").fetchone()
    conn.close()
    return row[0] if row else 0


def get_unique_maps(excluded_maps: list[str] = None,
                    allowed_sources: Optional[list[str]] = None) -> list[str]:
    """Return sorted list of unique map names, optionally excluding some.

    `allowed_sources` filters the underlying layer_cache rows to those tagged
    with one of the given source names. None or empty list = no source filter.
    """
    where, params = _source_filter(allowed_sources, prefix=" WHERE ")
    sql = f"SELECT DISTINCT map_name FROM layer_cache{where} ORDER BY map_name"
    conn = _get_conn()
    rows = conn.execute(sql, params).fetchall()
    conn.close()
    maps = [r[0] for r in rows]
    if excluded_maps:
        maps = [m for m in maps if not any(m.lower().startswith(e.lower()) for e in excluded_maps)]
    return maps


def get_modes_for_map(map_name: str, allowed_gamemodes: list[str] = None,
                      allowed_sources: Optional[list[str]] = None) -> list[dict]:
    """Return available mode+version combos for a map.

    Returns list of dicts with keys: gamemode, layer_version, raw_name, source, display.
    `allowed_sources` filters layers to those whose source is in the list.
    """
    src_clause, src_params = _source_filter(allowed_sources)
    sql = (
        f"SELECT gamemode, layer_version, raw_name, source FROM layer_cache "
        f"WHERE map_name = ?{src_clause} ORDER BY gamemode, layer_version"
    )
    conn = _get_conn()
    rows = conn.execute(sql, [map_name, *src_params]).fetchall()
    conn.close()

    results = []
    for gamemode, version, raw_name, source in rows:
        if allowed_gamemodes and gamemode not in allowed_gamemodes:
            continue
        display = gamemode
        if version:
            display = f"{gamemode} {version}"
        results.append({
            "gamemode": gamemode,
            "layer_version": version,
            "raw_name": raw_name,
            "source": source,
            "display": display,
        })
    return results


def get_layer_by_raw_name(raw_name: str,
                          allowed_sources: Optional[list[str]] = None) -> Optional[dict]:
    """Return full layer data by raw_name.

    `allowed_sources` filters to layers whose source is in the list. When the
    same raw_name exists in multiple matching sources, the first row found wins.
    """
    src_clause, src_params = _source_filter(allowed_sources)
    sql = (
        "SELECT raw_name, source, map_name, map_id, gamemode, layer_version, "
        "factions_json, team1_allowed_alliances, team2_allowed_alliances "
        f"FROM layer_cache WHERE raw_name = ?{src_clause} LIMIT 1"
    )
    conn = _get_conn()
    row = conn.execute(sql, [raw_name, *src_params]).fetchone()
    conn.close()
    if row is None:
        return None
    return {
        "raw_name": row[0],
        "source": row[1],
        "map_name": row[2],
        "map_id": row[3],
        "gamemode": row[4],
        "layer_version": row[5],
        "factions": json.loads(row[6]),
        "team1_allowed_alliances": json.loads(row[7]),
        "team2_allowed_alliances": json.loads(row[8]),
    }


def get_unique_factions(allowed_sources: Optional[list[str]] = None) -> list[str]:
    """Return sorted list of all unique faction IDs from cached layers."""
    where, params = _source_filter(allowed_sources, prefix=" WHERE ")
    sql = f"SELECT factions_json FROM layer_cache{where}"
    conn = _get_conn()
    rows = conn.execute(sql, params).fetchall()
    conn.close()
    factions = set()
    for (fj,) in rows:
        for f in json.loads(fj):
            if isinstance(f, dict) and "factionId" in f:
                factions.add(f["factionId"])
            elif isinstance(f, str):
                factions.add(f)
    return sorted(factions)


def get_unique_unit_types(allowed_sources: Optional[list[str]] = None) -> list[str]:
    """Return sorted list of all unique unit type names from cached layers."""
    where, params = _source_filter(allowed_sources, prefix=" WHERE ")
    sql = f"SELECT factions_json FROM layer_cache{where}"
    conn = _get_conn()
    rows = conn.execute(sql, params).fetchall()
    conn.close()
    units = set()
    for (fj,) in rows:
        for f in json.loads(fj):
            if isinstance(f, dict):
                for ut in f.get("unitTypes", []):
                    if isinstance(ut, dict):
                        units.add(ut.get("type", ""))
                    elif isinstance(ut, str):
                        units.add(ut)
    units.discard("")
    return sorted(units)


def get_unique_gamemodes(allowed_sources: Optional[list[str]] = None) -> list[str]:
    """Return sorted list of all unique gamemodes from cached layers."""
    where, params = _source_filter(allowed_sources, prefix=" WHERE ")
    sql = f"SELECT DISTINCT gamemode FROM layer_cache{where} ORDER BY gamemode"
    conn = _get_conn()
    rows = conn.execute(sql, params).fetchall()
    conn.close()
    return [r[0] for r in rows]


def get_map_sizes(allowed_sources: Optional[list[str]] = None) -> "dict[str, float]":
    """Return {map_name: max_layer_size_km} from the cache.

    A map's canonical size is taken as the largest size among its layers, since
    skirmish/seed sub-layers reuse the same mapId with a smaller playable area.
    Maps with no usable size data (all NULL) are omitted.
    """
    where, params = _source_filter(allowed_sources, prefix=" WHERE ")
    where = (where + " AND " if where else " WHERE ") + "map_size_km IS NOT NULL"
    sql = f"SELECT map_name, MAX(map_size_km) FROM layer_cache{where} GROUP BY map_name"
    conn = _get_conn()
    rows = conn.execute(sql, params).fetchall()
    conn.close()
    return {name: size for name, size in rows if size is not None}


def get_unique_sources() -> list[str]:
    """Return sorted list of distinct source names currently in the cache."""
    conn = _get_conn()
    rows = conn.execute(
        "SELECT DISTINCT source FROM layer_cache ORDER BY source"
    ).fetchall()
    conn.close()
    return [r[0] for r in rows]


